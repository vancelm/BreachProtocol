namespace BreachProtocol
{
    /// <summary>
    /// Represents the axis for limiting moves in a breach protocol puzzle.
    /// </summary>
    public enum PuzzleAxis
    {
        /// <summary>
        /// Represents a horizontal axis allowing only column moves.
        /// </summary>
        Horizontal = 0,

        /// <summary>
        /// Represents a vertical axis allowing only row moves.
        /// </summary>
        Vertical = 1
    }
}